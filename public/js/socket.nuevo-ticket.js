// Comando para establecer la conexion
var socket = io();

var label = $('#lblNuevoTicket');

/*=================================================================================================================================*/
// Conexión al servidor
/*=================================================================================================================================*/

socket.on('connect', function() {

    console.log('Conectado al servidor');

});

/*=================================================================================================================================*/
// Desconexión al servidor
/*=================================================================================================================================*/

socket.on('disconnect', function() {

    console.log('Desconectado al servidor');

});

/*=================================================================================================================================*/
// Estado actual de ticket
/*=================================================================================================================================*/

socket.on('estadoActual', function(estadoActual) {

    label.text(estadoActual.actual);

});

/*=================================================================================================================================*/
// Al hacer click el botón emite al servidor un cambio de ticket ( que pase al siguiente )
/*=================================================================================================================================*/

$('button').on('click', function() {

    socket.emit('siguienteTicket', null, function(siguiente) {

        label.text(siguiente);

    });

});